// Impicit Typecasting 

#include <iostream>
using namespace std;

int main(){
    char grade = 'a' ;

    int value = grade;

    cout<< value << endl;
    return 0;

}


// explicit Typecasting 

#include <iostream>
using namespace std;

int main(){
    double grade = 10.6 ;

    int value = (int)grade;

    cout<< value << endl;
    return 0;

}